    private boolean validarCamposObrigatorios() {

        // Titulo do livro
        if (txtTitulo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Informe Título do Livro!");
            txtTitulo.requestFocus();
            return false;
        }

        // ISBN
        if (txtISBN.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Informe ISBN do Livro!");
            txtISBN.requestFocus();
            return false;
        }
        // Ano do Livro
        if (txtAnoTitulo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Informe Ano do Livro!");
            txtAnoTitulo.requestFocus();
            return false;
        }
// Editora (JComboBox)
        String editoraSelecionado = cblEditora.getSelectedItem().toString();
        if (editoraSelecionado.equals("-----------------")) {
            JOptionPane.showMessageDialog(this,
                    "Selecione a Editora!");
            cblEditora.requestFocus();
            return false;
        }

        return true; // tudo OK
    }
