    private void carregarReservas() {
        DefaultTableModel model = (DefaultTableModel) tblReserva.getModel();
        model.setRowCount(0); // limpa a tabela

        String sql = "SELECT "
                + "r.id AS reserva_id, "
                + "t.titulo, "
                + "r.data_reserva, "
                + "ac.tombo, "
                + "t.ano, "
                + "ac.id,"
                + "GROUP_CONCAT(a.nome ORDER BY a.nome SEPARATOR ', ') AS autores "
                + "FROM reserva r "
                + "JOIN acervo ac ON r.acervo_id = ac.id "
                + "JOIN titulo t ON ac.titulo_id = t.id "
                + "LEFT JOIN titulo_autor ta ON t.id = ta.titulo_id "
                + "LEFT JOIN autor a ON a.id = ta.autor_id "
                + "WHERE r.usuario_id = ? "
                + "AND r.status = ? "
                + "GROUP BY "
                + "r.id, t.titulo, r.data_reserva, ac.tombo, t.ano "
                + "ORDER BY r.data_reserva DESC";

        boolean possuiReserva = false;
        scrollReserva.setVisible(true);
        try (Connection conn = Conexao.getConnection(); PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setInt(1, idAluno);
            ps.setString(2, "ATIVA");

            try (ResultSet rs = ps.executeQuery()) {
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

                while (rs.next()) {
                    possuiReserva = true;

                    model.addRow(new Object[]{
                        rs.getString("reserva_id"),
                        rs.getString("titulo"),
                        rs.getString("autores"),
                        sdf.format(rs.getTimestamp("data_reserva")),
                        rs.getString("tombo"),
                        rs.getInt("ano"),
                        rs.getInt("id")
                    });
                }

            }

            // Se n√£o encontrou reservas
            if (!possuiReserva) {
                JOptionPane.showMessageDialog(this,
                        "Aluno n√£o possui reserva.",
                        "Informa√ß√£o",
                        JOptionPane.INFORMATION_MESSAGE);

                scrollReserva.setVisible(false);
            } else {
                ajustarLarguraColunas();
                ocultarColunaIdReserva();
                JOptionPane.showMessageDialog(this,
                        "Selecione uma Reserva de uma obra para efetivar o EMPR√âSTIMO.",
                        "Informa√ß√£o",
                        JOptionPane.INFORMATION_MESSAGE);

                scrollReserva.setVisible(true);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao carregar reservas: " + e.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

