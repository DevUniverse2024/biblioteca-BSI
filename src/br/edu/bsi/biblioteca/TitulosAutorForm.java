    private javax.swing.JComboBox<AutorItem> criarComboAutoresNaoAssociados(int idTitulo) {

        javax.swing.JComboBox<AutorItem> combo = new javax.swing.JComboBox<>();

        try {
            Connection conn = Conexao.getConnection();

            String sql
                    = "SELECT id, nome "
                    + "FROM autor "
                    + "WHERE id NOT IN ( "
                    + "    SELECT autor_id "
                    + "    FROM titulo_autor "
                    + "    WHERE titulo_id = ? "
                    + ") "
                    + "ORDER BY nome";

            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, idTitulo);

            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                combo.addItem(new AutorItem(
                        rs.getInt("id"),
                        rs.getString("nome")
                ));
            }

            rs.close();
            ps.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao carregar autores disponíveis: " + e.getMessage());
        }

        return combo;
    }

    private void adicionarAutorAoTitulo(int idTitulo, int idAutor) {

        try {
            Connection conn = Conexao.getConnection();

            String sql
                    = "INSERT INTO titulo_autor (titulo_id, autor_id) VALUES (?, ?)";

            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, idTitulo);
            ps.setInt(2, idAutor);

            ps.executeUpdate();

            ps.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao adicionar autor: " + e.getMessage());
        }
    }

