    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        if (!validarCamposObrigatorios()) {
            return;
        }
        try {
            Connection conn = Conexao.getConnection();

            String sql
                    = "INSERT INTO titulo ( titulo, isbn, ano, editora) "
                    + "VALUES (?, ?, ?, ?)";

            PreparedStatement ps = conn.prepareStatement(sql);

            // Status vindo do ButtonGroup
            String status = "";

            ps.setString(1, txtTitulo.getText());
            ps.setString(2, txtISBN.getText());
            ps.setString(3, txtAnoTitulo.getText());
            ps.setString(4, cblEditora.getSelectedItem().toString());

            ps.executeUpdate();

            ps.close();
            conn.close();

            JOptionPane.showMessageDialog(this,
                    "Título incluído com sucesso!!!");
            limparTela();
            carregarTitulos();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao incluir: " + e.getMessage());
        }

    }//GEN-LAST:event_btnIncluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // 1️⃣ Validação: precisa ter ID
        if (txtIdTitulo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Selecione um Título na lista para alterar.");
            return;
        }
        if (!validarCamposObrigatorios()) {
            return;
        }
        try {
            Connection conn = Conexao.getConnection();

            String sql
                    = "UPDATE titulo "
                    + "SET titulo = ?, isbn = ?, ano = ?, editora = ? "
                    + "WHERE id = ?";

            PreparedStatement ps = conn.prepareStatement(sql);

            // 2️⃣ Status via RadioButton
            ps.setString(1, txtTitulo.getText());
            ps.setString(2, txtISBN.getText());
            ps.setString(3, txtAnoTitulo.getText());
            ps.setString(4, cblEditora.getSelectedItem().toString());
            ps.setInt(5, Integer.parseInt(txtIdTitulo.getText()));

            ps.executeUpdate();

            ps.close();
            conn.close();

            // 3️⃣ Feedback ao usuário
            JOptionPane.showMessageDialog(this,
                    "Título alterado com sucesso!");

            // 4️⃣ Limpa e recarrega
            limparTela();
            carregarTitulos();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao alterar Título: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        int idSelecionado = Integer.parseInt(txtIdTitulo.getText());

        // Desabilita botões durante a confirmação
        btnIncluir.setEnabled(false);
        btnAlterar.setEnabled(false);

        int opcao = JOptionPane.showConfirmDialog(
                this,
                "Confirma a exclusão do Título selecionado?",
                "Confirmação",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
        );

        // Se cancelar
        if (opcao != JOptionPane.YES_OPTION) {
            btnIncluir.setEnabled(true);
            btnAlterar.setEnabled(true);
            return;
        }

        try {
            Connection conn = Conexao.getConnection();

            String sql = "DELETE FROM titulo WHERE id = ?";

            PreparedStatement ps = conn.prepareStatement(sql);

            ps.setInt(1, Integer.parseInt(txtIdTitulo.getText()));

            ps.executeUpdate();

            ps.close();
            conn.close();

            JOptionPane.showMessageDialog(this,
                    "Título excluído com sucesso!");

            // Volta para tela inicial
            limparTela();
            carregarTitulos();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao excluir Título: " + e.getMessage());
        }


    }//GEN-LAST:event_btnExcluirActionPerformed

    private void tblTituloMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTituloMouseClicked
        // Se quiser exigir duplo clique, descomente:
        // if (evt.getClickCount() != 2) return;
        int linha = tblTitulos.getSelectedRow();

        if (linha == -1) {
            return;
        }

        // Recupera dados da JTable
        txtIdTitulo.setText(tblTitulos.getValueAt(linha, 0).toString());
        txtTitulo.setText(tblTitulos.getValueAt(linha, 1).toString());
        txtISBN.setText(tblTitulos.getValueAt(linha, 2).toString());
        txtAnoTitulo.setText(tblTitulos.getValueAt(linha, 3).toString());
        String editora = tblTitulos.getValueAt(linha, 4).toString();
        cblEditora.setSelectedItem(editora);

        // Habilita botões
        btnIncluir.setEnabled(false);
        btnAlterar.setEnabled(true);
        btnExcluir.setEnabled(true);
    }//GEN-LAST:event_tblTituloMouseClicked

