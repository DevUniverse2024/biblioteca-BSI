    private void calcularEPreencherDataDevolucao() {
        LocalDate dataBase = LocalDate.now();
        LocalDate dataDevolucao = adicionarDiasUteis(dataBase, 5);

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        txtDataDevolucao.setText(dataDevolucao.format(formatter));
    }

    private LocalDate adicionarDiasUteis(LocalDate dataInicial, int diasUteis) {
        LocalDate data = dataInicial;
        int adicionados = 0;

        while (adicionados < diasUteis) {
            data = data.plusDays(1);

            if (data.getDayOfWeek() != DayOfWeek.SATURDAY
                    && data.getDayOfWeek() != DayOfWeek.SUNDAY) {
                adicionados++;
            }
        }
        return data;
    }

