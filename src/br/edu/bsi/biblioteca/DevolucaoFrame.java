    private void atualizarrEmprestimo() {
        String idReservaTexto = txtIdEmprestimo.getText().trim();

        // Converte para int
        int idEmprestimo;
        try {
            idEmprestimo = Integer.parseInt(idReservaTexto);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "ID da reserva inv√°lido.",
                    "Erro",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        String dataPrevistaTexto = txtDataDevolucaoPrevista.getText().trim(); // pega a data prevista
        // Define o formato da data da tela
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        // Converte a data prevista para LocalDate
        LocalDate dataPrevista;
        try {
            dataPrevista = LocalDate.parse(dataPrevistaTexto, formatter);
        } catch (DateTimeParseException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Data prevista inv√°lida. Use o formato dd/MM/yyyy.",
                    "Erro",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

// Define o status com base na compara√ß√£o
        LocalDate dataAtual = LocalDate.now();
        String status_Finalizacao = dataAtual.isAfter(dataPrevista) ? "FINALIZADO COM ATRASO" : "FINALIZADO SEM ATRASO"; // SQL para atualizar status e data_devolucao
        String sql = "UPDATE emprestimo "
                + "SET status_emprestimo = ?, data_devolucao = ? "
                + "WHERE id = ?";

        try (Connection conn = Conexao.getConnection(); PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setString(1, status_Finalizacao); // status definido dinamicamente
            ps.setDate(2, java.sql.Date.valueOf(dataAtual)); // data de devolu√ß√£o = hoje
            ps.setInt(3, idEmprestimo);

            int linhasAfetadas = ps.executeUpdate();

            if (linhasAfetadas > 0) {
                JOptionPane.showMessageDialog(
                        this,
                        "Emprestimo atualizado com sucesso.",
                        "Sucesso",
                        JOptionPane.INFORMATION_MESSAGE
                );
            } else {
                JOptionPane.showMessageDialog(
                        this,
                        "N√£o foi poss√≠vel atualizar a 'empre¬¥stimo'. Verifique o ID.",
                        "Aten√ß√£o",
                        JOptionPane.WARNING_MESSAGE
                );
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Erro ao atualizar emprestimo: " + e.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }

    private void atualizarAcervo(int acervoId) {

        String sql = "UPDATE acervo SET status = 'DISPONIVEL' WHERE id = ?";

        try (Connection conn = Conexao.getConnection(); PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setInt(1, acervoId);
            ps.executeUpdate();

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    private void carregarEmprestimos() {
        DefaultTableModel model = (DefaultTableModel) tblEmprestimos.getModel();
        model.setRowCount(0); // limpa a tabela

        String sql = "SELECT e.id AS emprestimo_id, "
                + "       a.id AS acervo_id, "
                + "       t.titulo, "
                + "       e.data_emprestimo, "
                + "       e.data_prevista_devolucao, "
                + "       e.status_emprestimo "
                + "FROM emprestimo e "
                + "INNER JOIN acervo a ON e.acervo_id = a.id "
                + "INNER JOIN titulo t ON a.titulo_id = t.id "
                + "WHERE e.usuario_id = ? "
                + "AND e.status_emprestimo = 'EM ANDAMENTO'";

        try (Connection conn = Conexao.getConnection(); PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setInt(1, idAluno);

            try (ResultSet rs = ps.executeQuery()) {
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

                while (rs.next()) {
                    model.addRow(new Object[]{
                        rs.getString("emprestimo_id"),
                        rs.getString("titulo"),
                        sdf.format(rs.getTimestamp("data_emprestimo")),
                        sdf.format(rs.getTimestamp("data_prevista_devolucao")),
                        rs.getString("status_emprestimo"),
                        rs.getInt("acervo_id") // ID do acervo (int) no final
                    });
                }

            }

            //ajustarLarguraColunas();
            //ocultarColunaIdReserva();
            JOptionPane.showMessageDialog(this,
                    "Selecione um Empr√©stimo de uma obra para efetivar a DEVOLU√á√ÉO.",
                    "Informa√ß√£o",
                    JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao carregar reservas: " + e.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

