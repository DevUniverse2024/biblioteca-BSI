    private void limparTela() {

        // Campos de texto
        if (txtIdTitulo != null) {
            txtIdTitulo.setText("");
        }
        if (txtTitulo != null) {
            txtTitulo.setText("");
        }
        if (txtISBN != null) {
            txtISBN.setText("");
        }

        if (txtAnoTitulo != null) {
            txtAnoTitulo.setText("");
        }

        if (cblEditora != null) {
            cblEditora.setSelectedIndex(0);
        }
        btnIncluir.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
    }

