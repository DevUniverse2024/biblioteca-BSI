    private void inserirEmprestimo() {

        String usuarioTexto = txtIdAluno.getText().trim();
        String acervoTexto = txtIdAcervo.getText().trim(); // Vamos assumir que o acervo_id est√° relacionado √† reserva selecionada
        String dataPrevista = txtDataDevolucao.getText().trim();

        // Valida campos
        if (usuarioTexto.isEmpty() || acervoTexto.isEmpty() || dataPrevista.isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "N√£o √© poss√≠vel efetivar o empr√©stimo. Campos obrigat√≥rios est√£o vazios.",
                    "Aten√ß√£o",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        int usuarioId;
        int acervoId;

        try {
            usuarioId = Integer.parseInt(usuarioTexto);
            acervoId = Integer.parseInt(acervoTexto);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "ID do usu√°rio ou acervo inv√°lido.",
                    "Erro",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        String sql = "INSERT INTO emprestimo "
                + "(usuario_id, acervo_id, data_emprestimo, data_prevista_devolucao, status_emprestimo, data_devolucao) "
                + "VALUES (?, ?, CURRENT_DATE, ?, 'EM ANDAMENTO', NULL)";

        try (Connection conn = Conexao.getConnection(); PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setInt(1, usuarioId);
            ps.setInt(2, acervoId);
            ps.setDate(3, java.sql.Date.valueOf(
                    LocalDate.parse(dataPrevista, java.time.format.DateTimeFormatter.ofPattern("dd/MM/yyyy"))
            ));

            int linhasAfetadas = ps.executeUpdate();

            if (linhasAfetadas > 0) {
                JOptionPane.showMessageDialog(
                        this,
                        "Empr√©stimo registrado com sucesso.",
                        "Sucesso",
                        JOptionPane.INFORMATION_MESSAGE
                );
            } else {
                JOptionPane.showMessageDialog(
                        this,
                        "N√£o foi poss√≠vel registrar o empr√©stimo.",
                        "Aten√ß√£o",
                        JOptionPane.WARNING_MESSAGE
                );
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Erro ao inserir empr√©stimo: " + e.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }

    private void atualizarAcervo() {
        String idReservaTexto = txtIdReseva.getText().trim();

        if (idReservaTexto.isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "Nenhuma reserva selecionada.",
                    "Aten√ß√£o",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        int idReserva;
        try {
            idReserva = Integer.parseInt(idReservaTexto);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "ID da reserva inv√°lido.",
                    "Erro",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        // Primeiro, buscar o acervo_id da reserva
        int acervoId = -1;
        String sqlBuscaAcervo = "SELECT acervo_id FROM reserva WHERE id = ?";

        try (Connection conn = Conexao.getConnection(); PreparedStatement psBusca = conn.prepareStatement(sqlBuscaAcervo)) {

            psBusca.setInt(1, idReserva);
            try (ResultSet rs = psBusca.executeQuery()) {
                if (rs.next()) {
                    acervoId = rs.getInt("acervo_id");
                } else {
                    JOptionPane.showMessageDialog(
                            this,
                            "Reserva n√£o encontrada.",
                            "Aten√ß√£o",
                            JOptionPane.WARNING_MESSAGE
                    );
                    return;
                }
            }

            // Agora, atualizar o status do acervo
            String sqlUpdate = "UPDATE acervo SET status = ? WHERE id = ?";
            try (PreparedStatement psUpdate = conn.prepareStatement(sqlUpdate)) {
                psUpdate.setString(1, "EMPRESTADO");
                psUpdate.setInt(2, acervoId);

                int linhasAfetadas = psUpdate.executeUpdate();
                if (linhasAfetadas > 0) {
                    JOptionPane.showMessageDialog(
                            this,
                            "Status do acervo atualizado para 'EMPRESTADO'.",
                            "Sucesso",
                            JOptionPane.INFORMATION_MESSAGE
                    );
                } else {
                    JOptionPane.showMessageDialog(
                            this,
                            "N√£o foi poss√≠vel atualizar o status do acervo.",
                            "Aten√ß√£o",
                            JOptionPane.WARNING_MESSAGE
                    );
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Erro ao atualizar acervo: " + e.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }

