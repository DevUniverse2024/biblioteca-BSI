    private void btnAdicionarAutorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarAutorActionPerformed

        // Verifica se há título selecionado
        if (txtIdTitulo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Selecione um título primeiro.");
            return;
        }

        int idTitulo = Integer.parseInt(txtIdTitulo.getText());

        // Cria ComboBox dinamicamente
        javax.swing.JComboBox<AutorItem> comboAutores
                = criarComboAutoresNaoAssociados(idTitulo);

        int opcao = JOptionPane.showConfirmDialog(
                this,
                comboAutores,
                "Selecione um autor",
                JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.PLAIN_MESSAGE
        );

        if (opcao == JOptionPane.OK_OPTION) {

            AutorItem autorSelecionado
                    = (AutorItem) comboAutores.getSelectedItem();

            if (autorSelecionado != null) {

                // Insere no banco
                adicionarAutorAoTitulo(idTitulo, autorSelecionado.getId());

                // Atualiza a tabela de autores
                carregarAutoresDasObras(idTitulo);
            }
        }


    }//GEN-LAST:event_btnAdicionarAutorActionPerformed

    private void btnRemoverAutorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverAutorActionPerformed

        if (idAutorSelecionado == -1 || txtIdTitulo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Selecione um autor para remover.");
            return;
        }

        int idTitulo = Integer.parseInt(txtIdTitulo.getText());

        int confirmacao = JOptionPane.showConfirmDialog(
                this,
                "Deseja remover este autor da obra?",
                "Confirmação",
                JOptionPane.YES_NO_OPTION
        );

        if (confirmacao == JOptionPane.YES_OPTION) {

            removerAutorDoTitulo(idTitulo, idAutorSelecionado);

            // Atualiza tabela
            carregarAutoresDasObras(idTitulo);

            // Reseta seleção
            idAutorSelecionado = -1;
            btnRemoverAutor.setEnabled(false);
        }


    }//GEN-LAST:event_btnRemoverAutorActionPerformed
    private void removerAutorDoTitulo(int idTitulo, int idAutor) {

        try {
            Connection conn = Conexao.getConnection();

            String sql
                    = "DELETE FROM titulo_autor "
                    + "WHERE titulo_id = ? AND autor_id = ?";

            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, idTitulo);
            ps.setInt(2, idAutor);

            ps.executeUpdate();

            ps.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao remover autor: " + e.getMessage());
        }
    }

