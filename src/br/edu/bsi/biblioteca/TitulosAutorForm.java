    private void carregarTitulos() {
        btnAdicionarAutor.setEnabled(false);
        btnRemoverAutor.setEnabled(false);
        DefaultTableModel model
                = (DefaultTableModel) tblTitulos.getModel();

        model.setRowCount(0); // limpa a tabela
        ajustarColunasTitulos();
        try {
            Connection conn = Conexao.getConnection();

            String sql
                    = "SELECT id, titulo,  editora "
                    + "FROM titulo "
                    + "ORDER BY editora ASC, titulo ASC ";

            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("titulo"),
                    rs.getString("editora")
                });
            }

            rs.close();
            ps.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao carregar Títulos: " + e.getMessage());
        }
    }

    private void carregarAutoresDasObras(int idTitulo) {

        DefaultTableModel model
                = (DefaultTableModel) tblAutores.getModel();

        model.setRowCount(0); // limpa a tabela

        try {
            Connection conn = Conexao.getConnection();

            String sql
                    = "SELECT a.id, a.nome "
                    + "FROM autor a "
                    + "JOIN titulo_autor ta ON ta.autor_id = a.id "
                    + "WHERE ta.titulo_id = ? "
                    + "ORDER BY a.nome";

            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, idTitulo); // parâmetro do título

            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("nome")
                });
            }

            rs.close();
            ps.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao carregar autores da obra: " + e.getMessage());
        }
    }

